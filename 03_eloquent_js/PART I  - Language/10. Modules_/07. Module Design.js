//  - Structuring programs is one of the subtler aspects of programming. Any nontrivial piece of functionality 
//    can be modeled in various ways.

//  - The best way to learn the value of well-structured design is to read or work on a lot of programs and notice what works and 
//    what doesn’t. Don’t assume that a painful mess is “just the way it is.” 

//  - ----You can improve the structure of almost everything by putting more thought into it----

//  - One aspect of module design is ease of use. If you are designing something that is intended to be used by multiple people 
//    or even by yourself, in three months when you no longer remember the specifics of what you did it is helpful if your interface
//    is SIMPLE and PREDICTABLE. That may mean following existing conventions.

//  - Even if there’s no standard function or widely used package to imitate, you can keep your modules predictable by 
//    using simple data structures and doing a single, focused thing.


//  - Focused modules that compute values are applicable in a wider range of programs than bigger modules that perform 
//    complicated actions with side effects.


